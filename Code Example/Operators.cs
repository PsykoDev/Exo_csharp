namespace DefaultNamespace
{
    public class Operators
    {
        
        public static void Operator()
        {
            int a = 10;
            int b = 20;
            
            if(a == b) // si a est égal à b
            if(a != b) // si a est différent de b ou alors si a n'es pas égale à b
            if(a > b) // si a est supérieur à b
            if(a < b) // si a est inférieur à b
            if(a >= b) // si a est supérieur ou égal à b
            if(a <= b) // si a est inférieur ou égal à b
            if(a == b && a == b) // si a est égal à b et aussi a est égal à b
            if(a == b & a == b) // si a est égal à b et a est égal à b
            if(a == b || a == b) // si a est égal à b ou sinon a est égal à
            if(a == b | a == b) // si a est égal à b ou a est égal à b
            
        }       
    }
}

